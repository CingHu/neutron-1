import abc
import six


@six.add_metaclass(abc.ABCMeta)
class ServiceDriverBase(object):
    @abc.abstractmethod
    def process(self, device_ids, removed_devices_info):
        pass

    @abc.abstractmethod
    def collect_state(self, configurations):
        pass
